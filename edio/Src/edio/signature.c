typedef struct {
    u8 signature[4];
    u8 date_time[4];
    u8 serial_l[4];
    u8 serial_g[4];
    u8 hv_key[8];
    u8 rnd_table[8];
    u8 uid_xor[8];
    u8 date_time_xor[4];
    u8 device_id;
    u8 manufac_id;
    u8 info_srv[2];
    u8 crc_not[2];
    u8 crc[2];
    u8 hv_rev;
    u8 info_host[3];
    u8 reserved[8];
} Edsg;

u32 sigParseNum(u8 *data, u8 bytes);
u8 sigCheckKey(u8 *key, u8 *uid);

Signature signature;

void sigInit() {

    Edsg edsg; // = (Edsg *) ADDR_PFL_EDSG;
    u16 crc;
    u16 crc_calc;

    memcpy(&edsg, (void *) ADDR_PFL_EDSG, sizeof (Edsg));
    memset(&signature, 0, sizeof (Signature));

    flaGetUID(signature.flash_uid);
    signature.date = sigParseNum(&edsg.date_time[0], 2);
    signature.time = sigParseNum(&edsg.date_time[2], 2);
    signature.serial_l = sigParseNum(edsg.serial_l, 4);
    signature.serial_g = sigParseNum(edsg.serial_g, 4);
    signature.manufac_id = edsg.manufac_id;
    signature.hv_rev = edsg.hv_rev;
    signature.device_id = edsg.device_id;
    crc = sigParseNum(edsg.crc, 2);
    crc_calc = crcFast(&edsg, 0x30);
    if (signature.serial_l == 0xF001 && signature.serial_g == 0xF002)signature.dev_mode = 1;

    //unxor all stuff
    for (int i = 0; i < 4; i++) {
        edsg.date_time_xor[i] ^= edsg.rnd_table[i];
        edsg.uid_xor[i + 0] ^= edsg.rnd_table[i + 0];
        edsg.uid_xor[i + 4] ^= edsg.rnd_table[i + 4];
    }
    edsg.crc_not[0] ^= 0xff;
    edsg.crc_not[1] ^= 0xff;

    //mapper number will be scrambled if not pass
    //****************************************************************************** signature validation    
    if (crc != crc_calc)return;

    if (edsg.crc[0] != edsg.crc_not[0])return;
    if (edsg.crc[1] != edsg.crc_not[1])return;

    for (int i = 0; i < 4; i++) {
        if (edsg.date_time_xor[i] != edsg.date_time[i])return;
        if (edsg.uid_xor[i + 0] != signature.flash_uid[i + 0])return;
        if (edsg.uid_xor[i + 4] != signature.flash_uid[i + 4])return;
    }


    if (sigCheckKey(edsg.hv_key, signature.flash_uid) != 0)return;

    if (signature.valid == 0)signature.valid ^= 1;
}

u32 sigParseNum(u8 *data, u8 bytes) {

    u32 val = 0;

    while (bytes--) {
        val <<= 8;
        val |= *data++;
    }

    return val;
}

u8 sigCheckKey(u8 *key, u8 *uid) {

    u16 i;
    u8 val;
    u8 acc = 0;
    u8 hv_key[32];


    for (i = 0; i < 8; i++) {
        hv_key[0 + i * 2] = uid[i];
        hv_key[1 + i * 2] = key[i];
    }
    memcpy(&hv_key[16], &hv_key[0], 16);

    //hardware key validation
    for (i = 0; i < 32; i++) {

        val = (edsg_config[hv_key[i]] + i / 2 % 8);

        if (i < 16) {
            if ((i & 1) == 0) acc += val;
        } else {
            if ((i & 1) == 0) acc = (acc & 1) == 0 ? acc + val : acc - val;
            if ((i & 1) == 1 && acc != hv_key[i])return 1;
        }
    }

    return 0;
}

u8 sigSet(u8 *data) {

    u8 buff[sizeof (Edsg)];
    u8 sig_eq, sig_blank;

    memcpy(buff, (void *) ADDR_PFL_EDSG, sizeof (Edsg));

    sig_eq = 1;
    sig_blank = 1;
    for (int i = 0; i < sizeof (Edsg); i++) {
        if (buff[i] != 0xff)sig_blank = 0;
        if (buff[i] != data[i])sig_eq = 0;
    }

    if (sig_eq)return 0;
    if (!sig_blank)return ERR_SIG_NBLA;
    
    mcuProgData(data, ADDR_PFL_EDSG, sizeof (Edsg));
    
    return 0;
}
